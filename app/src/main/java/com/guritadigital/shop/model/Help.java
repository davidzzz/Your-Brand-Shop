package com.guritadigital.shop.model;

public class Help {
    private int id;
    private String judul, isi;

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getJudul() {
        return judul;
    }

    public void setIsi(String isi) {
        this.isi = isi;
    }

    public String getIsi() {
        return isi;
    }
}
